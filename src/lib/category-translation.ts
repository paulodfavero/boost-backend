/**
 * Translates a category description from English to Portuguese.
 * If the category is not found in the list, returns the original category.
 *
 * @param category - The category description in English (or already in Portuguese)
 * @returns The translated category description in Portuguese
 */
export function translateCategory(category: string | null | undefined): string {
  if (!category) {
    return category || ''
  }

  // Try to find exact match first
  const exactMatch = categoryList.find(
    (item) => item.description.toLowerCase() === category.toLowerCase(),
  )

  if (exactMatch) {
    return exactMatch.descriptionTranslated
  }

  // If not found, return the original category (might already be in Portuguese or custom)
  return category
}
